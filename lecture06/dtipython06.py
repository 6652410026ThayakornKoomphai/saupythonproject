# คำสั่ง For


for x in range(5):
    print(f"DTI-{x}")

print("Hello world")

print("--------------------------")

for y in range(2, 5):
    print(f"SAU-{y}")

print("Hello world")

print("--------------------------")

for z in range(2, 5, 3):
    print(f"WOW-{z}")

print("Hello world")

print("--------------------------")

for a in range(3, 30, 5):
    print(f'HI{a}')

print("Hello world")

