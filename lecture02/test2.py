#ต่อ/รวม/เชื่อม ข้อมูลหลายๆประเภทเข้าด้วยกัน
#ใช้ ,
score = 500
print("hello...",20,True,555.444,10/2,"hi...",score)
#ใช้ +
print("hello...."+str(29)+str(True)+str(444.555)+str(19/20)+"Hi..."+str(score))
#ใช้ เมธอด format
print("hello.. {} {} {} {} Hi... {}.format(20,True,222.333,10/20,score")
#ใช้ f -string
print(f"hello... {20} {True} {300.200} {10/2} hi... {score} ")
#ใช้ modular operator %
print("hello... %d %f %s Hi..." % (20, 16.5, "sau") )